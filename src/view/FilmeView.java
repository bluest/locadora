    public void preencheComboTipo(){
        try {
            listaTipos=tipoDAO.ListaTipo();
        } catch (SQLException ex) {
            Logger.getLogger(FilmeView.class.getName()).log(Level.SEVERE, null, ex);
        }
        for(Tipo tipo:listaTipos){
            cbxTipo.addItem(tipo.getDescricao());
        }
    
    }
    
    public void atualizarTabelaFilme() {
        filme = new Filme();
        try {
            listaFilmes = filmeDAO.ListaFilme();
        } catch (SQLException ex) {
            Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
        }
        String dados[][] = new String[listaFilmes.size()][6];
        int i = 0;
        for (Filme filme : listaFilmes) {
            dados[i][0] = String.valueOf(filme.getCodigo());
            dados[i][1] = filme.getTitulo();
            dados[i][2] = String.valueOf(filme.getPreco());
            dados[i][3] = filme.getGenero();
            dados[i][4] = filme.getTipo().getDescricao();
            dados[i][5] = filme.getDescricao();
            i++;
        }
        String tituloColuna[] = {"Código", "Título", "Preço", "Gênero", "Tipo", "Descrição"};
        DefaultTableModel tabelaFilme = new DefaultTableModel();
        tabelaFilme.setDataVector(dados, tituloColuna);
        tblFilme.setModel(new DefaultTableModel(dados, tituloColuna) {
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();

        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        tblFilme.setRowHeight(25);
        tblFilme.updateUI();

    }

