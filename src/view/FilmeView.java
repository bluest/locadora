    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if(txtTitulo.getText().isEmpty()||txtPreco.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            txtTitulo.requestFocusInWindow();
        } else if(txtCodigo.getText().isEmpty()){
            filme = new Filme();
            filme.setTitulo(txtTitulo.getText());
            filme.setPreco(Double.parseDouble(txtPreco.getText()));
            filme.setGenero(txtGenero.getText());
            filme.setDescricao(txtDescricao.getText());
            filme.setTipo(listaTipos.get(cbxTipo.getSelectedIndex()));
            try {
                filmeDAO.salvar(filme);
            } catch (SQLException ex) {
                Logger.getLogger(FilmeView.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Gravado com sucesso");
            atualizarTabelaFilme();
            preparaSalvarECancelar();
            desativaCampos();
            limpaCamposFilme();
        }else{
            filme = new Filme();
            filme.setCodigo(Integer.parseInt(txtCodigo.getText()));
            filme.setTitulo(txtTitulo.getText());
            filme.setPreco(Double.parseDouble(txtPreco.getText()));
            filme.setGenero(txtGenero.getText());
            filme.setDescricao(txtDescricao.getText());
            filme.setTipo(listaTipos.get(cbxTipo.getSelectedIndex()));
            try {
                filmeDAO.alterar(filme);
            } catch (SQLException ex) {
                Logger.getLogger(FilmeView.class.getName()).log(Level.SEVERE, null, ex);
            }
            atualizarTabelaFilme();
            preparaSalvarECancelar();
            desativaCampos();
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void tblFilmeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFilmeMouseClicked
        // TODO add your handling code here:
        txtCodigo.setText(tblFilme.getValueAt(tblFilme.getSelectedRow(), 0).toString());
        txtTitulo.setText(tblFilme.getValueAt(tblFilme.getSelectedRow(), 1).toString());
        txtPreco.setText(tblFilme.getValueAt(tblFilme.getSelectedRow(), 2).toString());
        txtGenero.setText(tblFilme.getValueAt(tblFilme.getSelectedRow(), 3).toString());
        cbxTipo.setSelectedItem(tblFilme.getValueAt(tblFilme.getSelectedRow(), 4).toString());
        txtDescricao.setText(tblFilme.getValueAt(tblFilme.getSelectedRow(), 5).toString());
        preparaSelecaoTabela();
    }//GEN-LAST:event_tblFilmeMouseClicked

    private void limpaCamposFilme(){
        txtCodigo.setText("");
        txtTitulo.setText("");
        txtPreco.setText("");
        txtGenero.setText("");
        txtDescricao.setText("");
    }
    
    private void ativaCampos(){
        txtTitulo.setEnabled(true);
        txtPreco.setEnabled(true);
        txtGenero.setEnabled(true);
        txtDescricao.setEnabled(true);
        cbxTipo.setEnabled(true);
    }
    
    private void desativaCampos(){
        txtTitulo.setEnabled(false);
        txtPreco.setEnabled(false);
        txtGenero.setEnabled(false);
        txtDescricao.setEnabled(false);
        cbxTipo.setEnabled(false);
    }
    
    private void preparaNovo(){
        btnNovo.setEnabled(false);
        btnGravar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        tblFilme.setEnabled(false);
        tblFilme.clearSelection();
    }
    
    private void preparaSalvarECancelar(){
        btnNovo.setEnabled(true);
        btnGravar.setEnabled(false);
        btnCancelar.setEnabled(false);
        tblFilme.setEnabled(true);
    }
    
    private void preparaAlterar(){
        btnNovo.setEnabled(false);
        btnGravar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        tblFilme.setEnabled(false);
        tblFilme.clearSelection();
    }
    
    private void preparaExcluir(){
    	btnNovo.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
    }
    
    private void preparaSelecaoTabela(){
        btnNovo.setEnabled(false);
        btnAlterar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }
    
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if(txtCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um filme");
        }
        else{
            filme = new Filme();
            filme.setCodigo(Integer.parseInt(txtCodigo.getText()));
            int confirma=JOptionPane.showConfirmDialog(null, "Deseja excluir "+txtTitulo.getText()+"?");
            if(confirma==0){
                try {
                    filmeDAO.excluir(filme);
                } catch (SQLException ex) {
                    Logger.getLogger(FilmeView.class.getName()).log(Level.SEVERE, null, ex);
                }
                limpaCamposFilme();
                atualizarTabelaFilme();
                preparaExcluir();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

