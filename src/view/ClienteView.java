    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().isEmpty() || txtEndereco.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            txtNome.requestFocusInWindow();
        } else if (txtCodigo.getText().isEmpty()){
            cliente = new Cliente();
            cliente.setNomeCliente(txtNome.getText());
            cliente.setEnderecoCliente(txtEndereco.getText());
            cliente.setBairroCliente(txtBairro.getText());
            try {
                clienteDAO.salvar(cliente);

            } catch (SQLException ex) {
                Logger.getLogger(ClienteView.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Gravado com sucesso");
            atualizarTabelaCliente();
            preparaSalvarECancelar();
            desativaCampos();
            limpaCamposCliente();
        }else{
        	cliente = new Cliente();
        	cliente.setCodigoCliente(Integer.parseInt(txtCodigo.getText()));
            cliente.setNomeCliente(txtNome.getText());
            cliente.setEnderecoCliente(txtEndereco.getText());
            cliente.setBairroCliente(txtBairro.getText());
            try {
                clienteDAO.alterar(cliente);

            } catch (SQLException ex) {
                Logger.getLogger(ClienteView.class
                        .getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Gravado com sucesso");
            atualizarTabelaCliente();
            preparaSalvarECancelar();
            desativaCampos();
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClienteMouseClicked
        // TODO add your handling code here:
        txtCodigo.setText(tblCliente.getValueAt(tblCliente.getSelectedRow(), 0).toString());
        txtNome.setText(tblCliente.getValueAt(tblCliente.getSelectedRow(), 1).toString());
        txtEndereco.setText(tblCliente.getValueAt(tblCliente.getSelectedRow(),2).toString());
        txtBairro.setText(tblCliente.getValueAt(tblCliente.getSelectedRow(),3).toString());
        preparaSelecaoTabela();
    }//GEN-LAST:event_tblClienteMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if(txtCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um cliente");
        }
        else{
            cliente=new Cliente();
            cliente.setCodigoCliente(Integer.parseInt(txtCodigo.getText()));
            int confirma = JOptionPane.showConfirmDialog(null, "Deseja excluir " + txtNome.getText() + "?");
            if (confirma == 0){
                try {
                    clienteDAO.excluir(cliente);
                } catch (SQLException ex) {
                    Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
                }
                limpaCamposCliente();
                atualizarTabelaCliente();
                preparaExcluir();
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

