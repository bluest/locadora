    public void limpaCamposCliente(){
        txtCodigo.setText("");
        txtNome.setText("");
        txtEndereco.setText("");
        txtBairro.setText("");
    }
    
    private void ativaCampos(){
        txtNome.setEnabled(true);
        txtBairro.setEnabled(true);
        txtCodigo.setEnabled(true);
        txtEndereco.setEnabled(true);
    }
    
    private void desativaCampos(){
        txtNome.setEnabled(false);
        txtBairro.setEnabled(false);
        txtCodigo.setEnabled(false);
        txtEndereco.setEnabled(false);
    }
    
    private void preparaNovo(){
        btnNovo.setEnabled(false);
        btnGravar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        tblCliente.setEnabled(false);
        tblCliente.clearSelection();
    }
    
    private void preparaSalvarECancelar(){
        btnNovo.setEnabled(true);
        btnGravar.setEnabled(false);
        btnCancelar.setEnabled(false);
        tblCliente.setEnabled(true);
    }
    
    private void preparaAlterar(){
        btnNovo.setEnabled(false);
        btnGravar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        tblCliente.setEnabled(false);
        tblCliente.clearSelection();
    }
    
    private void preparaExcluir(){
    	btnNovo.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
    }
    
    private void preparaSelecaoTabela(){
        btnNovo.setEnabled(false);
        btnAlterar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }
    

